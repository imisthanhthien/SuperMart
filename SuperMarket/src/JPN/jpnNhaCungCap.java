    private void btnThemNCCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemNCCActionPerformed
        // TODO add your handling code here:
        String tenNCC = txtTenNCC.getText().trim();
        String diachi = txtDiachi.getText().trim();
        String email = txtEmail.getText().trim();
        String sdt = txtSDT.getText().trim();
        if(tenNCC.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Tên nhà cung cấp không được để trống");
        }
        
         else if(diachi.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Địa chỉ nhà cung cấp không được để trống");
        }
         else if(sdt.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "SĐT không được để trống");
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email nhà cung cấp không được để trống");
        } else if (KT_NhaCC(email, sdt)) {
            JOptionPane.showMessageDialog(this, "Nhà cung cấp đã tồn tại vui lòng xem lại");

        } else {
            this.InsertNhaCungCap();
        }
        
    }//GEN-LAST:event_btnThemNCCActionPerformed

