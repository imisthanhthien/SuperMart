    private void LoadTableNhaCungCap() {
        Vector<NhaCungCap> dsncc = DAO.DAO_NhaCungCap.DS_NhaCungCap();
        for (NhaCungCap ncc : dsncc) {
            Vector v = new Vector();
            v.add(ncc.getMaNCC());
            v.add(ncc.getTenNCC());
            v.add(ncc.getDiaChi());
            v.add(ncc.getEmail());
            v.add(ncc.getSdt());
            dataNCC.add(v);
        }
    }

    public void InsertNhaCungCap() {
        String tenNCC = txtTenNCC.getText().trim();
        String diachi = txtDiachi.getText().trim();
        String email = txtEmail.getText().trim();
        String sdt = txtSDT.getText().trim();
        boolean KT = DAO.DAO_NhaCungCap.InsertNhaCungCap(tenNCC, diachi, email, sdt);
        if (KT) {
            JOptionPane.showMessageDialog(this, "Thêm thành công");
        }
        tblModelNCC.setRowCount(0);
        LoadTableNhaCungCap();
        tblNCC.updateUI();
    }

    private void deleteNhaCungCap() throws IOException {
        int pos = tblNCC.getSelectedRow();
        int maNCC = (int) tblNCC.getValueAt(pos, 0);

        boolean KT = DAO.DAO_NhaCungCap.DeleteNhaCungCap(maNCC);
        if (KT) {
            JOptionPane.showMessageDialog(this, "Xóa thành công");
        }
        tblModelNCC.setRowCount(0);
        LoadTableNhaCungCap();
        tblNCC.updateUI();
    }

    private void updateNhaCungCap() throws IOException {
        int pos = tblNCC.getSelectedRow();
        int maNCC = (int) tblNCC.getValueAt(pos, 0);

        String tenNCC = txtTenNCC.getText().trim();
        String diachi = txtDiachi.getText().trim();
        String sdt = txtSDT.getText().trim();
        String email = txtEmail.getText().trim();

        boolean KT = DAO.DAO_NhaCungCap.UpdateNhaCungCap(maNCC, tenNCC, diachi, sdt, email);
        if (KT) {
            JOptionPane.showMessageDialog(this, "Sửa thành công");
        }
        tblModelNCC.setRowCount(0);
        LoadTableNhaCungCap();
        tblNCC.updateUI();
    }
    public boolean KT_NhaCC(String email, String sdt)
    {
        try {
            String sql = "Select *from NhaCungCap";
            SQLProvider ncc = new SQLProvider();
            ncc.open();
            ResultSet rs= ncc.executeQuery(sql);
            while(rs.next())
            {
                if(email.equalsIgnoreCase(rs.getString(4)) || sdt.equalsIgnoreCase(rs.getString(5)))
                    return true;
            }
        } catch (Exception e) {
        }
        return false;
    }
